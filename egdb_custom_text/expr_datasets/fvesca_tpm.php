<p>
  Public RNA-seq datasets of <i>Fragaria vesca</i> are widely collected and curated to build this expression profile set.
  Because the collected tissues varied in sample number, developmental stage, and level of granularity, they were classified into six major tissue types (leaf, root, flower, fruit, seed, and stem) that shared high expression similarity. 
  Batch effects that primarily arose from project differences were removed, and the expression levels were normalized as TPM.
</p>

<p>
  This core dataset retains aggregated information for the six main tissue types.
  If more granular information is required, please consult the corresponding individual datasets.
  More detailed tissue classifications and developmental stages, defined separately in each dataset, are listed below:
  <ul>
    <li>Leaf at various developmental stages</li>
    <li>Root samples collected at different time intervals.</li>
    <li>Receptacle meristem, flower meristem, and flower at different stages.</li>
    <li>Fruit at various developmental stages</li>
    <li>Seed samples (no subgroup)</li>
    <li>Above-ground vegetative tissues (axillary buds, stem apices, crowns, and stems)</li>
  </ul>
  If you need to compare gene expression levels across different datasets (for example, leaf and fruit at different developmental stages), please utilize the expression comparator tools.
</p>

