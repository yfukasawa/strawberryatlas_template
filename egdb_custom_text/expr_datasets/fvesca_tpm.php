<p>
  Public RNA-seq datasets of <i>Fragaria vesca</i> are widely collected and curated to build this expression profile set.
  Due to variability in the collected tissues regarding sample number, developmental stages, and granularity, tissues were categorized into nine major types (leaf, root, flower bud, anthers, carpels, early fruit, late fruit, seed, and stem) based on their high similarity in expression profiles. Batch effects, primarily resulting from differences among projects, were removed, and expression levels were normalized as transcripts per million (TPM).
</p>

<p>
  The core dataset (all) contains aggregated data for the nine primary tissue types.
  For more detailed information, please refer to the individual datasets.
  Detailed tissue classifications and developmental stages, defined separately within each dataset, are listed below:
  <ul>
    <li>Leaves at different developmental stages</li>
    <li>Roots samples collected at different ages</li>
    <li>Flower buds</li>
    <li>Anthers at different developmental stages</li>
    <li>Carpels at different developmental stages</li> 
    <li>Fruits at early develpmental stages (cortex, pith, and carpels)</li>
    <li>Fruits at late developmental stages </li>
    <li>Seeds at different developmental stages (whole seed, embryo, seed coat, ovule)</li>
    <li>Above-ground vegetative tissues (stem apices, crowns, and stems)</li>
  </ul>
  If you need to compare gene expression levels across different datasets (for example, leaf and fruit at different developmental stages), please utilize the expression comparator tools.
</p>

